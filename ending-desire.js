function appendHighlightedText(paragraph, text) {
  const highlightPattern = /\[\[(.+?)\]\]/g;
  let lastIndex = 0;
  let match;

  while ((match = highlightPattern.exec(text)) !== null) {
    paragraph.append(text.slice(lastIndex, match.index));
    const highlight = document.createElement("strong");
    highlight.textContent = match[1];
    paragraph.append(highlight);
    lastIndex = match.index + match[0].length;
  }

  paragraph.append(text.slice(lastIndex));
}

